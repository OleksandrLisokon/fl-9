export function plus(a, b) {
  return a + b;
};

export function minus(a, b) {
  return a - b;
};

export function times(a, b) {
  return a * b;
};

export function divide(a, b) {
  return a / b;
};

export function percent(a, b) {
  return (a / 100) * b;
};

export function sqrt(a){
  return Math.sqrt(a);
};

export function square(a) {
  return a * a;
};